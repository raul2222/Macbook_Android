package es.upv.practicaprocesadob;

public class Clase {
    private String dato1;
    private double dato2;

    public Clase(String dato1, double dato2) {
        this.dato1 = dato1;
        this.dato2 = dato2;
    }

    public String getDato1() {
        return dato1;
    }

    public void setDato1(String dato1) {
        this.dato1 = dato1;
    }

    public double getDato2() {
        return dato2;
    }

    public void setDato2(double dato2) {
        this.dato2 = dato2;
    }


    public double getCampo1() {
        return dato2;
    }
}
